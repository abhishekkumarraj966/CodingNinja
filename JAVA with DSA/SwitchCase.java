public class SwitchCase {
    public static void main(String[] args) {
        int a=10;
        int b=30;
        int c=1,d;
        switch(c){
            case 1:
                d=a+b;
                System.out.println("Add  "+d);
                break;
            case 2:
                d=a-b;
                System.out.println("Sub "+d);
                break;
        }
    }
    
}
